// This script verifies that a minimal site contains only the barebones of a site.

import org.jsoup.Jsoup

// Verifies that all the files were created
[
    'target/site/index.html',
    'module/target/site/index.html'
].each {
    def file = new File(basedir, it)
    if (!file.exists()) {
        throw new IllegalStateException(
            "file ${file} doesn't exist"
        )
    }
}

// Parse HTML
def html = new File(basedir, 'target/site/index.html').text
def parsed = Jsoup.parse(html)

// Footer link
def div = parsed.body().select( 'footer #footer-info div' ).last()
assert div.html().contains( 'Rendered using' )

// Comments before the head
assert html.contains( 'Rendered using Docs Maven Skin' )
assert html.contains( 'Generated by Apache Maven Doxia' )

// Parse HTML
def htmlModule = new File(basedir, 'module/target/site/index.html').text
def parsedModule = Jsoup.parse(htmlModule)

// Footer link
def divModule = parsedModule.body().select( 'footer #footer-info div' ).last()
assert divModule.html().contains( 'Rendered using' )

// Comments before the head
assert htmlModule.contains( 'Rendered using Docs Maven Skin' )
assert htmlModule.contains( 'Generated by Apache Maven Doxia' )
